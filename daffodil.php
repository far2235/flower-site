    <h2>About Daffodils</h2>
    <p>
        A staple of spring gardens, daffodils bloom in a brilliant display of large yellow or
        white flowers. There are many varieties of daffodil with varying colors, shapes, and
        sizes. Daffodils are easy to grow in most regions in North America and are a versitile
        addition to any garden.
    </p>
    <h2>Caring for Daffodils</h2>
    <p>
        Daffodils prefer full sun, but can still do well in partial shade. Soil moisture isn't
        a big concern with daffodils, as long as the soil drainage is good enough to let most
        of the water pass through. Do not try to uncover and expose early buds, as the coverings
        often act as protection from harsh weather conditions. Be sure to continue watering your
        daffodils after their flowers have wilted.
    </p>
    <h2>Planting Daffodils</h2>
    <p>
        Daffodils should be planted in the fall, about a month before the ground freezes.
        Plant daffodil bulbs two or three times as deep as the bulb is tall- this typically means
        at least four inches down. Space bulbs about three to six inches apart. Typically, bigger
        bulbs yield bigger flowers, so choose large, healthy bulbs.
    </p>
