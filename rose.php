    <h2>Caring for Roses</h2>
    <p>
        Rose bushes require plenty of watering- at least twice weekly. Make sure the soil has good
        drainage though, and ensure the entire root zone is saturated. Plucking wilted flowers from
        rose bushes can promote more flowering and should be done at least once a week, but should
        be avoided at least a month before the first frost.
    </p>
    <h2>Planting Roses</h2>
    <p>
        When planting roses, wear thick gloves to protect yourself from the thorny stems. Dig a hole
        over a foot wide, and loosen the roots before planting. Once the rose bush is rooted, water
        it thoroughly. Do not overcrowd rose bushes; try to keep them at least a couple feet apart.
    </p>
