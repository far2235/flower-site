    <h2>About Crocuses</h2>
    <p>
        Crocuses come up in early spring (sometimes when the ground is still snowy!) with various
        vibrant colors that starkly contrast the bleak colors of winter. Crocuses are important
        for pollinators, being one of the few sources of nectar early in the year.
    </p>
    <h2>Caring for Crocuses</h2>
    <p>
        Crocuses will naturally spread and regrow each year, and are not bothered by common
        garden threats such as deer and rabbits. Crocuses prefer spots with full sun, but can
        still do well under partial sun. Make sure to water well after planting, and maintain soil
        moisture, especially in dry weather (but be careful to not over-water). Early bloomers should
        be protected from harsh weather with plastic coverings.
    </p>
    <h2>Planting Crocuses</h2>
    <p>
        Plant crocuses in the fall, several weeks before the ground freezes- they need to be kept
        in extended cold to bloom in the spring. Crocuses should be planted in early to
        mid fall in cold regions, and mid fall in warmer climates. Cluster bulbs together a few
        inches apart rather than spacing them out, and try to plant a dozen or more at a time.
    </p>
